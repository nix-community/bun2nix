use std::fs;
use std::os::unix::fs::symlink;
use std::path::{Component, Path};
use std::process::Command;

use crate::lockfile::BinField;

/// Validate that a bin command name is safe.
///
/// Rejects names containing `/`, `\`, null bytes, `.`, `..`, or empty strings.
fn validate_bin_name(name: &str) {
    assert!(!name.is_empty(), "bin name must not be empty");
    assert!(
        !name.contains('/'),
        "bin name must not contain '/': {name:?}"
    );
    assert!(
        !name.contains('\\'),
        "bin name must not contain '\\': {name:?}"
    );
    assert!(
        !name.contains('\0'),
        "bin name must not contain null bytes: {name:?}"
    );
    assert!(
        name != "." && name != "..",
        "bin name must not be '.' or '..'"
    );
}

/// Validate that a bin path stays within the package directory.
///
/// Walks the path components and asserts the result doesn't escape `package_path`.
fn validate_bin_path(package_path: &Path, bin_path_str: &str) {
    let resolved = package_path.join(bin_path_str);
    // Normalize by walking components — no filesystem access needed
    let mut normalized = package_path.to_path_buf();
    for component in Path::new(bin_path_str).components() {
        match component {
            Component::Normal(c) => normalized.push(c),
            Component::CurDir => {}
            Component::ParentDir => {
                normalized.pop();
            }
            _ => panic!("unexpected path component in bin path: {bin_path_str:?}"),
        }
    }
    assert!(
        normalized.starts_with(package_path),
        "bin path escapes package directory: {resolved:?} (from {bin_path_str:?}) is not under {}",
        package_path.display()
    );
}

/// Copy a package from the cache to the target path.
///
/// Uses `cp -rL` to dereference symlinks and `--no-preserve=mode,ownership`
/// to ensure files are writable (Nix store files are read-only).
pub fn copy_package(cache_path: &Path, target_path: &Path) {
    let status = Command::new("cp")
        .args(["-rL", "--no-preserve=mode,ownership"])
        .arg(cache_path)
        .arg(target_path)
        .status()
        .expect("failed to execute cp");

    if !status.success() {
        eprintln!(
            "Warning: cp failed for {} -> {}",
            cache_path.display(),
            target_path.display()
        );
    }
}

/// Create a symlink for a workspace package.
pub fn symlink_workspace(workspace_path: &Path, target_path: &Path) {
    if let Err(e) = symlink(workspace_path, target_path) {
        if e.kind() != std::io::ErrorKind::AlreadyExists {
            eprintln!(
                "Warning: failed to symlink workspace {} -> {}: {}",
                workspace_path.display(),
                target_path.display(),
                e
            );
        }
    }
}

/// Create bin links in the `.bin/` directory for a package.
pub fn create_bin_links(package_path: &Path, bin_field: &BinField, bin_dir: &Path) {
    fs::create_dir_all(bin_dir).ok();

    match bin_field {
        BinField::Single(bin_path) => {
            // Read package.json to get the package name for the bin command name.
            let pkg_json_path = package_path.join("package.json");
            let name = match fs::read_to_string(&pkg_json_path)
                .ok()
                .and_then(|s| serde_json::from_str::<serde_json::Value>(&s).ok())
                .and_then(|v| v.get("name")?.as_str().map(String::from))
            {
                Some(n) => n,
                None => return,
            };

            // Use the unscoped name as the bin name.
            let bin_name = if name.contains('/') {
                name.split('/').last().unwrap_or(&name)
            } else {
                &name
            };

            validate_bin_name(bin_name);
            validate_bin_path(package_path, bin_path);

            let target = pathdiff(bin_dir, &package_path.join(bin_path));
            let link_path = bin_dir.join(bin_name);
            symlink_ignore_exists(&target, &link_path);
        }
        BinField::Map(entries) => {
            for (name, path) in entries {
                validate_bin_name(name);
                validate_bin_path(package_path, path);

                let target = pathdiff(bin_dir, &package_path.join(path));
                let link_path = bin_dir.join(name);
                symlink_ignore_exists(&target, &link_path);
            }
        }
    }
}

/// Compute a relative path from `from` directory to `to` file.
fn pathdiff(from: &Path, to: &Path) -> std::path::PathBuf {
    // Use a simple implementation: find common prefix, then go up with ..
    let from = fs::canonicalize(from).unwrap_or_else(|_| from.to_path_buf());
    let to_abs = if to.is_absolute() {
        to.to_path_buf()
    } else {
        std::env::current_dir().unwrap_or_default().join(to)
    };

    let mut from_parts: Vec<_> = from.components().collect();
    let mut to_parts: Vec<_> = to_abs.components().collect();

    // Remove common prefix
    while !from_parts.is_empty() && !to_parts.is_empty() && from_parts[0] == to_parts[0] {
        from_parts.remove(0);
        to_parts.remove(0);
    }

    let mut result = std::path::PathBuf::new();
    for _ in &from_parts {
        result.push("..");
    }
    for part in &to_parts {
        result.push(part);
    }

    result
}

fn symlink_ignore_exists(target: &Path, link_path: &Path) {
    if let Err(e) = symlink(target, link_path) {
        if e.kind() != std::io::ErrorKind::AlreadyExists {
            eprintln!(
                "Warning: failed to create bin link {} -> {}: {}",
                link_path.display(),
                target.display(),
                e
            );
        }
    }
}
